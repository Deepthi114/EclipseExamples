
public class SwitchCase {

	public static void main(String[] args) {
		int a=7;
		switch (a) {
		case 5:
			System.out.println("Value is :"+a);
			break;
		case 6:
			System.out.println("Value is :"+a);
			break;
		case 7:
			System.out.println("Value is :"+a);
			break;

		default:
			System.out.println("None of the above");
			break;
		}
	}

}
